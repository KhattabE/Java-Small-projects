public class Calculator {

    //Addition calculation
    int add(int a, int b){
        return a + b;
    }

    //Subtraction calculation
    int subtract(int a, int b){
        return a - b;
    }

    //Multiplication calculation
    int multiply(int a, int b){
        return a * b;
    }

    //Division calculation
    int divide(int a, int b){
        return a / b;
    }



}
